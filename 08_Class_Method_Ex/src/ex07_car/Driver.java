package ex07_car;

public class Driver {

  // Field
  private String  name;
  private int     career;
  private boolean bestDriver;
  
  
  // Constructor
  
  
  // Setter
  public void setName(String name) {
    this.name = name;
  }
  
  public void setCareer(int career) {
    this.career = career;
  }
  
  public void setBestDrinver(boolean bestDriver) {
    this.bestDriver = bestDriver;
  }
  
  // Getter
  public String getName() {
    return name;
  }
  
  public int getCareer() {
    return career;
  }
  
  public boolean isBestDriver() {
    return bestDriver;
  }
  // ★ boolean 타입은 getter의 이름규칙이 다르다.
  // ★ ' get' 이 아닌 ' is ' 로 적어줘야 한다. 
  
  
  // Method

 }
