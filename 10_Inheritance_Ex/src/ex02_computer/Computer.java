package ex02_computer;

public class Computer {

  
  // Field
  private String model;
  
  // Constructor
  public Computer(String model) {
    this.model = model;
  }
  
  // Getter & Setter
  public String getModel() {
    return model;
  }
  
  public void setModel(String model) {
    this.model = model;
  }
  
  
  
}
